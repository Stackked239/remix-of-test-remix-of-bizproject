export interface QuestionOption {
  value: number;
  label: string;
  description: string;
}

export interface Question {
  id: string;
  fieldName: string; // Maps to pipeline JSON field
  questionNumber: number;
  text: string;
  type: 'scale' | 'percentage' | 'currency' | 'numeric' | 'text' | 'select' | 'checkbox' | 'composite';
  options?: QuestionOption[];
  selectOptions?: { value: string; label: string }[];
  placeholder?: string;
  unit?: string;
  hasEstimateFlag?: boolean;
  estimateFlagField?: string;
  hasNaOption?: boolean;
  naFlagField?: string;
  hasFollowUp?: boolean;
  followUpField?: string;
  followUpCondition?: number; // Show follow-up if value < this
  required?: boolean;
  min?: number;
  max?: number;
  step?: number;
}

export interface Dimension {
  code: string;
  name: string;
  description: string;
  jsonKey: string; // Maps to pipeline JSON section
  questions: Question[];
}

export interface Chapter {
  code: string;
  name: string;
  description: string;
  transitionMessage: string;
  dimensions: Dimension[];
}

